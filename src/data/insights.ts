// Get insights page content (in a real app, this would fetch from a database)
export const getInsightsPageContent = (): InsightsPageContent => {
  // Check if there's stored content in localStorage (browser only)
  if (typeof window !== 'undefined') {
    const storedContent = localStorage.getItem('insightsPageContent');
    if (storedContent) {
      try {
        // Parse stored content and ensure it's of the correct type
        const parsedContent = JSON.parse(storedContent) as InsightsPageContent;
        return parsedContent;
      } catch (e) {
        console.error('Failed to parse stored insights page content:', e);
      }
    }
  }

  // Return default content if nothing is stored or we're on the server
  return defaultInsightsPageContent;
};

// Save insights page content (in a real app, this would save to a database)
export const saveInsightsPageContent = (content: InsightsPageContent): boolean => {
  try {
    if (typeof window !== 'undefined') {
      localStorage.setItem('insightsPageContent', JSON.stringify(content));
    }
    return true;
  } catch (e) {
    console.error('Failed to save insights page content:', e);
    return false;
  }
};
